<?php

namespace FaimfonyBundle\Entity;

use Doctrine\Common\Collections\ArrayCollection;
use FOS\UserBundle\Model\User as BaseUser;
use Doctrine\ORM\Mapping as ORM;
use Gedmo\Mapping\Annotation as Gedmo;


use Symfony\Component\Validator\Constraints as Assert;


/**
 * @ORM\Entity
 * @ORM\Table(name="user")
 */
class User extends BaseUser
{
    /**
     * @ORM\Id
     * @ORM\Column(type="integer")
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    protected $id;

    /**
     * @ORM\Column(name="first_name", type="string", length=255)
     *
     */
    protected $first_name;

    /**
     * @ORM\Column(name="last_name", type="string", length=255)
     */
    protected $last_name;

    /**
     * @ORM\Column(name="phone", type="string", length=10)
     */
    protected $phone;

    /**
     * @var string $slug;
     *
     * @Gedmo\Slug(fields={"first_name", "last_name"}, updatable=false, separator="-")
     * @ORM\Column(name="slug", type="string", length=255)
     */
    protected $slug;

    /**
     * @ORM\Column(name="user_type", type="boolean")
     */
    protected $is_cooker;

    /**
     * @ORM\OneToMany(targetEntity="Restaurant", mappedBy="user")
     */
    protected $restaurants;

    /**
     * @ORM\ManyToMany(targetEntity="Meal", inversedBy="users")
     * @ORM\JoinTable(name="users_favorites")
     */
    protected $favorites;


    public function __construct()
    {
        parent::__construct();
        $this->favorites = new ArrayCollection();
        $this->restaurants = new ArrayCollection();
    }

    /**
     * Set firstName
     *
     * @param string $firstName
     *
     * @return User
     */
    public function setFirstName($firstName)
    {
        $this->first_name = $firstName;

        return $this;
    }

    /**
     * Get firstName
     *
     * @return string
     */
    public function getFirstName()
    {
        return $this->first_name;
    }

    /**
     * Set lastName
     *
     * @param string $lastName
     *
     * @return User
     */
    public function setLastName($lastName)
    {
        $this->last_name = $lastName;

        return $this;
    }

    /**
     * Get lastName
     *
     * @return string
     */
    public function getLastName()
    {
        return $this->last_name;
    }

    /**
     * Set phone
     *
     * @param integer $phone
     *
     * @return User
     */
    public function setPhone($phone)
    {
        $this->phone = $phone;

        return $this;
    }

    /**
     * Get phone
     *
     * @return integer
     */
    public function getPhone()
    {
        return $this->phone;
    }

    /**
     * Set slug
     *
     * @param string $slug
     *
     * @return User
     */
    public function setSlug($slug)
    {
        $this->slug = $slug;

        return $this;
    }

    /**
     * Get slug
     *
     * @return string
     */
    public function getSlug()
    {
        return $this->slug;
    }

    public function setEmail($email)
    {
        $this->setUsername($email);
        return parent::setEmail($email); // TODO: Change the autogenerated stub

    }

    /**
     * Add restaurant
     *
     * @param \FaimfonyBundle\Entity\Restaurant $restaurant
     *
     * @return User
     */
    public function addRestaurant(\FaimfonyBundle\Entity\Restaurant $restaurant)
    {
        $this->restaurants[] = $restaurant;

        return $this;
    }

    /**
     * Remove restaurant
     *
     * @param \FaimfonyBundle\Entity\Restaurant $restaurant
     */
    public function removeRestaurant(\FaimfonyBundle\Entity\Restaurant $restaurant)
    {
        $this->restaurants->removeElement($restaurant);
    }

    /**
     * Get restaurants
     *
     * @return \Doctrine\Common\Collections\Collection
     */
    public function getRestaurants()
    {
        return $this->restaurants;
    }

    /**
     * Add favorite
     *
     * @param \FaimfonyBundle\Entity\Meal $favorite
     *
     * @return User
     */
    public function addFavorite(\FaimfonyBundle\Entity\Meal $favorite)
    {
        $this->favorites[] = $favorite;

        return $this;
    }

    /**
     * Remove favorite
     *
     * @param \FaimfonyBundle\Entity\Meal $favorite
     */
    public function removeFavorite(\FaimfonyBundle\Entity\Meal $favorite)
    {
        $this->favorites->removeElement($favorite);
    }

    /**
     * Get favorites
     *
     * @return \Doctrine\Common\Collections\Collection
     */
    public function getFavorites()
    {
        return $this->favorites;
    }

    /**
     * Set isCooker
     *
     * @param boolean $isCooker
     *
     * @return User
     */
    public function setIsCooker($isCooker)
    {
        $this->is_cooker = $isCooker;

        return $this;
    }

    /**
     * Get isCooker
     *
     * @return boolean
     */
    public function getIsCooker()
    {
        return $this->is_cooker;
    }
}
